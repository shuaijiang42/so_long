#include "so_long_bonus.h"

void	free_map(char **map)
{
	int	i;

	i = 0;
	while (map[i])
	{
		free(map[i]);
		i++;
	}
	free(map);
}

void	free_game(t_game *game)
{
	free_map(game->map);
	free_map(game->f_col_map);
	free_map(game->f_map);
	free(game);
}
